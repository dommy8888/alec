// Function to populate the lists with items for both Dommy and Alec
function populateFavorites() {
    const dommySongsList = document.getElementById('dommySongsList');
    const dommyArtistsList = document.getElementById('dommyArtistsList');
    const dommyAlbumsList = document.getElementById('dommyAlbumsList');
    const alecSongsList = document.getElementById('alecSongsList');
    const alecArtistsList = document.getElementById('alecArtistsList');
    const alecAlbumsList = document.getElementById('alecAlbumsList');

    // Dommy's favorites
    favorites.dommy.songs.forEach(song => {
        const li = document.createElement('li');
        li.textContent = song.title;
        li.onclick = () => showDetails('song', song.title, 'dommy');
        dommySongsList.appendChild(li);
    });

    favorites.dommy.artists.forEach(artist => {
        const li = document.createElement('li');
        li.textContent = artist;
        li.onclick = () => showDetails('artist', artist, 'dommy');
        dommyArtistsList.appendChild(li);
    });

    favorites.dommy.albums.forEach(album => {
        const li = document.createElement('li');
        li.textContent = album.title;
        li.onclick = () => showDetails('album', album.title, 'dommy');
        dommyAlbumsList.appendChild(li);
    });

    // Alec's favorites
    favorites.alec.songs.forEach(song => {
        const li = document.createElement('li');
        li.textContent = song.title;
        li.onclick = () => showDetails('song', song.title, 'alec');
        alecSongsList.appendChild(li);
    });

    favorites.alec.artists.forEach(artist => {
        const li = document.createElement('li');
        li.textContent = artist;
        li.onclick = () => showDetails('artist', artist, 'alec');
        alecArtistsList.appendChild(li);
    });

    favorites.alec.albums.forEach(album => {
        const li = document.createElement('li');
        li.textContent = album.title;
        li.onclick = () => showDetails('album', album.title, 'alec');
        alecAlbumsList.appendChild(li);
    });
}

// Function to show details of a selected item
function showDetails(type, title, person) {
    const homePage = document.getElementById('homePage');
    const detailsPage = document.getElementById('detailsPage');
    const detailTitle = document.getElementById('detailTitle');
    const detailContent = document.getElementById('detailContent');

    // Hide homepage content and show details page
    homePage.style.display = 'none';
    detailsPage.style.display = 'block';

    let content = '';

    if (type === 'album') {
        const album = favorites[person].albums.find(a => a.title === title);
        if (album) {
            content = `
                <p><strong>Release Date:</strong> ${album.releaseDate}</p>
                <p><strong>Runtime:</strong> ${album.runTime}</p>
                <h3>Tracklist:</h3>
                <ul>
                    ${album.tracks.map(track => `<li>${track}</li>`).join('')}
                </ul>
            `;
        }
    } else if (type === 'artist') {
        content = `<p>${title}</p>`;
    } else if (type === 'song') {
        const song = favorites[person].songs.find(s => s.title === title);
        if (song) {
            content = `
                <p><strong>Artist:</strong> ${song.artist}</p>
                <p><strong>Release Date:</strong> ${song.releaseDate}</p>
                <p><strong>Duration:</strong> ${song.duration}</p>
            `;
        }
    }

    detailTitle.textContent = `${person.charAt(0).toUpperCase() + person.slice(1)}'s ${type.charAt(0).toUpperCase() + type.slice(1)}: ${title}`;
    detailContent.innerHTML = content || `<p>Details for ${title} are not available.</p>`;
}

// Function to go back to the home page
function goBack() {
    const homePage = document.getElementById('homePage');
    const detailsPage = document.getElementById('detailsPage');

    // Show homepage content and hide details page
    homePage.style.display = 'block';
    detailsPage.style.display = 'none';
}